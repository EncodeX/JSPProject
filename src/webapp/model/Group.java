public class Group {
    private int groID;
    private String groName;
    private int subNum;

    public int getGroID() {
        return groID;
    }

    public void setGroID(int groID) {
        this.groID = groID;
    }

    public String getGroName() {
        return groName;
    }

    public void setGroName(String groName) {
        this.groName = groName;
    }

    public int getSubNum() {
        return subNum;
    }

    public void setSubNum(int subNum) {
        this.subNum = subNum;
    }
}
